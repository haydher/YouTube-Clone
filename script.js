function fillCard() {
 const mainContent = document.querySelector(".mainContent");

 for (let i = 0; i < data.length; i++) {
  const card = document.createElement("div");
  const cardImgContainer = document.createElement("div");
  const videoLink = document.createElement("a");
  const thumbnail = document.createElement("img");
  const vidDuration = document.createElement("p");
  const videoInfoContainer = document.createElement("div");
  const logo = document.createElement("div");
  const channelLogo = document.createElement("img");
  const channelLink = document.createElement("a");
  const videoInfo = document.createElement("div");
  const title = document.createElement("h1");
  const channelName = document.createElement("p");
  const viewContainer = document.createElement("div");
  const views = document.createElement("p");
  const uploadTime = document.createElement("p");

  card.classList.add("card");
  cardImgContainer.classList.add("cardImgContainer");
  vidDuration.classList.add("vidDuration");
  videoInfoContainer.classList.add("videoInfoContainer");
  logo.classList.add("logo");
  videoInfo.classList.add("videoInfo");
  title.classList.add("title");
  channelName.classList.add("channelName");
  viewContainer.classList.add("viewContainer");
  views.classList.add("views");
  uploadTime.classList.add("uploadTime");

  thumbnail.src = data[i].thumbnail;
  vidDuration.innerHTML = data[i].vidDuration;

  cardImgContainer.appendChild(thumbnail);
  cardImgContainer.appendChild(vidDuration);

  videoLink.href = data[i].videoLink;
  videoLink.appendChild(cardImgContainer);

  channelLogo.src = data[i].logo;

  channelLink.href = data[i].channelLink;
  channelLink.appendChild(channelLogo);

  logo.appendChild(channelLink);

  title.innerHTML = data[i].title;
  channelName.innerHTML = data[i].channelName;

  views.innerHTML = `${data[i].views} views`;
  uploadTime.innerHTML = data[i].uploadTime;

  viewContainer.appendChild(views);
  viewContainer.appendChild(uploadTime);

  videoInfo.appendChild(title);
  videoInfo.appendChild(channelName);
  videoInfo.appendChild(viewContainer);

  videoInfoContainer.appendChild(logo);
  videoInfoContainer.appendChild(videoInfo);

  card.appendChild(videoLink);
  card.appendChild(videoInfoContainer);

  mainContent.appendChild(card);
 }
}

const menu = document.querySelector(".menu");
const tabs = document.querySelectorAll(".tabs");
const p = document.querySelectorAll(".tabs p");
const asideTitle = document.querySelectorAll(".asideTitle");
const collapsedMenu = document.querySelectorAll(".collapsedMenu");
const aside = document.querySelector("aside");
const divider = document.querySelectorAll(".divider");
const footer = document.querySelector(".footer");
const mainContent = document.querySelector(".mainContent");
const mainNavFilter = document.querySelector(".mainNavFilter");

menu.addEventListener("click", () => {
 tabs.forEach((e) => {
  if (!e.classList.contains("minimize")) e.classList.toggle("toggleMenu");
 });

 collapsedMenu.forEach((e) => e.classList.toggle("collapsedMenuToggle"));

 p.forEach((e) => e.classList.toggle("toggleMenu"));

 asideTitle.forEach((e) => e.classList.toggle("toggleMenu"));

 divider.forEach((e) => e.classList.toggle("toggleMenu"));

 aside.classList.toggle("asideToggleSize");

 footer.classList.toggle("toggleMenu");

 mainNavFilter.classList.toggle("mainNavFilterCollapsed");

 mainContent.classList.toggle("mainContentMenuCollapsed");
});
